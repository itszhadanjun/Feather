int sm_sum (int num1, int num2)
{
    return num1 + num2;
}

int sm_subtraction (int num1, int num2)
{
    return num1 - num2;
}

int sm_multiplication (int num1, int num2)
{
    return num1 * num2;
}

int sm_division (int num1, int num2)
{
    return num1 / num2;
}